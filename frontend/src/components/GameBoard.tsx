    let icon = '';
    let iconClass = '';
    
    if (block.type === 'bomb') {
      icon = 'üí£';
      iconClass = 'bomb-icon';
    } else if (block.type === 'rainbow') {
      icon = 'üåà';
      iconClass = 'rainbow-icon';
    } else if (block.type === 'line') {
      icon = '‚ö°';
      iconClass = 'line-icon';
    }
    
    const isSelected = selectedBlocks.some(b => b.row === row && b.col === col);
    
