        const boardWidth = boardRef.current.clientWidth - 24; // ÂáèÂéªÂÜÖËæπË∑ù
        const calculatedSize = Math.floor((boardWidth - (cols - 1) * 8) / cols); // ËÄÉËôëÈó¥Èöô
